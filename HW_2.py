x = int(input('Пожалуйста введите 5-значное целое число: '))

x1 = int(x // 10000)
x2 = int((x // 1000 * 1000 - x1 * 10000) / 1000)
x3 = int((x // 100 * 100 - x1 * 10000 - x2 * 1000) / 100)
x4 = int((x // 10 * 10 - x1 * 10000 - x2 * 1000 - x3 * 100) / 10)
x5 = int(x - x1 * 10000 - x2 * 1000 - x3 * 100 - x4 * 10)

y = int(x5 * 10000 + x4 * 1000 + x3 * 100 + x2 * 10 + x1)

print(x, ' ---> ', y)
